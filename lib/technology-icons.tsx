/**
 * Extract the "base" technology key from a slug.
 *
 * Examples:
 *   "react-development"                  → "react"
 *   "react-development-for-saas"         → "react"
 *   "nextjs-development"                 → "nextjs"
 *   "nodejs-development-for-fintech"     → "nodejs"
 *   "laravel-application-development"    → "laravel"
 *   "python-django-development-for-saas" → "python"
 *   "mern-stack-development"            → "mern"
 *   "framework-selection"               → null (no brand icon)
 */
function extractBaseKey(slug: string): string | null {
  const parts = slug.split("-");

  // Compound prefix match (e.g., "nextjs", "nodejs")
  const compoundPrefixes = [
    "nextjs", "nodejs", "tailwindcss", "postgresql",
    "typescript", "javascript", "graphql", "amazonwebservices",
  ];
  for (const prefix of compoundPrefixes) {
    if (parts[0] === prefix || parts[0].startsWith(prefix)) {
      return prefix;
    }
    // Also check if the prefix is split across first two parts
    if (parts.length > 1 && `${parts[0]}-${parts[1]}` === prefix) {
      return prefix;
    }
  }

  // Single-word tech match
  const knownTechs = [
    "react", "laravel", "django", "python", "mern", "php",
    "mongodb", "redis", "docker", "kubernetes", "aws", "vue",
    "angular", "svelte", "go", "rust", "graphql", "express",
  ];
  for (const tech of knownTechs) {
    if (parts[0] === tech || parts[0].startsWith(tech)) {
      return tech;
    }
  }

  // Fallback: scan all parts for any known tech word
  for (const part of parts) {
    if (knownTechs.includes(part)) {
      return part;
    }
  }

  return null;
}

/**
 * Get the icon component for a given technology slug.
 * Returns `null` if no matching brand icon exists.
 */
export function getTechIcon(slug: string): React.ComponentType<IconProps> | null {
  const key = extractBaseKey(slug);
  if (!key) return null;
  return techIconMap[key] ?? null;
}

/**
 * React component that renders the appropriate brand icon
 * for a given technology slug. Falls back to emoji text when
 * no brand icon is available.
 */
export function TechIcon({
  slug,
  className,
  size,
  fallback,
}: {
  slug: string;
  className?: string;
  size?: number;
  fallback?: string;
}) {
  const Icon = getTechIcon(slug);
  if (Icon) {
    return <Icon className={className} size={size} />;
  }
  return <span className={className}>{fallback ?? "⚙️"}</span>;
}
